function openArticle(id) {
  const a = articles[id];
  if (!a) return;
  document.getElementById('modalTags').innerHTML = a.tags.join('');
  document.getElementById('modalTitle').textContent = a.title;
  document.getElementById('modalMeta').innerHTML = a.meta;
  document.getElementById('modalContent').innerHTML = a.content;
  document.getElementById('articleModal').classList.add('show');
  document.body.style.overflow = 'hidden';
}

function closeModal() {
  document.getElementById('articleModal').classList.remove('show');
  document.body.style.overflow = '';
}

function closeOnBg(e) {
  if (e.target === document.getElementById('articleModal')) closeModal();
}

function filterCards(btn, cat) {
  document.querySelectorAll('.tb-filter-btn').forEach(b => b.classList.remove('active'));
  btn.classList.add('active');
  document.querySelectorAll('.tb-card').forEach(card => {
    if (cat === 'all' || card.dataset.cat === cat) {
      card.style.display = 'flex';
    } else {
      card.style.display = 'none';
    }
  });
}

document.querySelectorAll('.page-node').forEach(n => {
  n.addEventListener('click', e => {
    e.preventDefault();
    if (!n.classList.contains('prev') && !n.classList.contains('next')) {
      document.querySelector('.page-node.active')?.classList.remove('active');
      n.classList.add('active');
    }
  });
});